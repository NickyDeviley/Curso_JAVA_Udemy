package Secao5;

// AULA 46 - SEÇÃO 5
public class Aula46 {
	public static void main(String[] args) {

		// SWITCH CASE
		
		int a = 3;
		
		switch (a) {
		
		case 1:
			System.out.println("É 1");
			break;
		case 2:
			System.out.println("É 2");
			break;
		case 3:
			System.out.println("É 3");
			break;
		default:
			System.out.println("Não é nenhum dos 3");
			break;
		
		}
		
	}
}
