package Secao5;

// AULA 40 - SEÇÃO 5
public class Aula40 {
	public static void main(String[] args) {

		/*
			Operadores comparativos:
			
			< 	| menor que
			> 	| maior que
			<=	| menor ou igual a
			>=	| maior ou igual a
			==	| igual a
			!=	| Diferente de
		*/
		
		boolean a = true;
		
		a = 10 < 20;	// true
		a = 10 > 20;	// false
		a = 10 <= 20;	// true
		a = 10 >= 20;	// false
		a = 10 == 20;	// false
		a = 10 != 20;	// true
		
	}
}
