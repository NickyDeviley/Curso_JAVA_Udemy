public class Aula32 {
	public static void main(String[] args) {
	
		// Casting é uma tecnologia que se refere
		// a converter tipos de variáveis. Existem
		// dois tipos de type casting.
		
		// Type Casting Implícito, ocorre quando
		// atribuímos uma variável menor
		// em uma maior.
		
		int a = 150;
		int b = 200;
		double c = a + b;
		
		// Type Casting Explícito ocorre quando
		// o código nos obriga a declarar
		// a conversão de dados.
		
		double d = 150 + 200;
		int e = (int) d;	// Convertendo os dados
		

	}
}
