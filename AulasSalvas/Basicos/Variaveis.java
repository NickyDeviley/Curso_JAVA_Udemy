package Basicos;

public class Variaveis {

	public static void main() {
		
		byte val1 = 0;				// Variável byte
		short val2 = 1;				// Variável short
		int val3 = 2;				// Variável int
		long val4 = 3;				// Variável long
		
		float val5 = 4.1f;			// Variável float
		double val6 = 4.2;			// Variável Double
		
		char val7 = 'a';			// Variável char
		String val8 = "Nicholas";	// Variável String
		
		// Também é possível declarar várias variáveis de uma só vez.
		
		int a = 20, b = 30, c = 50;
		int a1, b1, c1;
		
	}
	
}
