package Composicao;

public class Comentario {
	
	private String conteudo;
	
	public Comentario() {}
	
	public Comentario(String conteudo) {
		
		this.conteudo = conteudo;
		
	}
	
	public String getComentario() {
		return this.conteudo;
	}
	
	public void setComentario(String conteudo) {
		this.conteudo = conteudo;
	}
	
}
